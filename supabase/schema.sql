-- Student Interest Matcher schema (users, interests, user_interests)

-- 1) Users table
create table if not exists users (
  id bigint generated by default as identity primary key,
  name text,
  class text,
  rollno text,
  email text,
  phone text,
  whatsapp text,
  instagram text,
  password text,
  created_at timestamp with time zone default now()
);

-- migration-safe adjustments for existing installations
alter table users add column if not exists name text;
alter table users add column if not exists class text;
alter table users add column if not exists rollno text;
alter table users add column if not exists email text;
alter table users add column if not exists phone text;
alter table users add column if not exists whatsapp text;
alter table users add column if not exists instagram text;
alter table users add column if not exists password text;
alter table users add column if not exists created_at timestamp with time zone default now();

alter table users drop column if exists auth_user_id;
alter table users drop constraint if exists users_email_kitsw_check;

create unique index if not exists users_email_unique_idx on users(email);

-- 2) Interests table
create table if not exists interests (
  id bigint generated by default as identity primary key,
  name text not null unique
);

-- 3) Many-to-many bridge table
create table if not exists user_interests (
  user_id bigint not null references users(id) on delete cascade,
  interest_id bigint not null references interests(id) on delete cascade,
  primary key (user_id, interest_id)
);

-- Helpful indexes for matching queries
create index if not exists idx_user_interests_user_id on user_interests(user_id);
create index if not exists idx_user_interests_interest_id on user_interests(interest_id);

