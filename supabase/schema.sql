-- RPC: directly callable via supabase.rpc('get_student_matches', { target_user_id: 1 })
create or replace function get_student_matches(target_user_id bigint)
returns table (
  id bigint,
  name text,
  class text,
  rollno text,
  email text,
  phone text,
  whatsapp text,
  instagram text,
  common_interest_count bigint
)
language sql
stable
as $$
  select
    u.id,
    u.name,
    u.class,
    u.rollno,
    u.email,
    u.phone,
    u.whatsapp,
    u.instagram,
    count(*) as common_interest_count
  from user_interests my_ui
  join user_interests other_ui on other_ui.interest_id = my_ui.interest_id
  join users u on u.id = other_ui.user_id
  where my_ui.user_id = target_user_id
    and other_ui.user_id <> target_user_id
  group by u.id, u.name, u.class, u.rollno, u.email, u.phone, u.whatsapp, u.instagram
  order by common_interest_count desc, u.name asc;
$$;

-- RPC: fetch interests for one user via supabase.rpc('get_user_interests', { target_user_id: 1 })
create or replace function get_user_interests(target_user_id bigint)
returns table (
  id bigint,
  name text
)
language sql
stable
as $$
  select i.id, i.name
  from user_interests ui
  join interests i on i.id = ui.interest_id
  where ui.user_id = target_user_id
  order by i.name;
$$;
